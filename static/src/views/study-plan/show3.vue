            因为自己发出声音和听到自己的声音，不仅需要用脑，还需要同时动用自己的发音器官和听觉器官，
            这样不仅增加了对脑子的刺需要用脑，还需要同时动用自己的发音器官和听觉器官，
            这样不仅增加了对脑子的刺激强度，也多建立了一些条件反射，能使记忆过程加速并巩固。
            先可以大声朗读单词、短语、句子和课文，水平稍有提高后可以朗读报并巩固。原因是由于念出声来可以使注意力集中。
          </p>
          <h3>7、培养学生眼到、口到、心到的良好朗读习惯。</h3>
