          <p>
            讲究卫生是每一个人应有的生活习惯。在现代社会中，小学生养成讲究清洁卫生的好习惯特别重要。它是一个人文明的表现，既体现了良好的个人面貌，又包含了对他人的尊重。
            细化目标：
            1、勤洗澡洗头，勤剪指甲，勤换衣服，睡前刷牙、洗脸，指甲内无污垢，身体无异味。
            2、饭前便后洗手。
            3、定期整理和清洗书包。
            4、爱护环境，不随地吐痰，不乱扔杂物。见到废纸等垃圾物要主动捡起放到果皮箱里。
            5、不在墙壁上乱写、乱画、乱贴、乱挂。6、不在小摊上买零食，不吃变质食物，不喝生水。
