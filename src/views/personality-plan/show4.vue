          <h1>学生文明行为习惯养成十大教育内容</h1>
          <p>学生文明行为习惯养成教育主要内容</p>
          <h3>一、举止文明的习惯</h3>
          <p>
            举止是指一个人在活动中的姿态。它包括站立、行走、就坐、手势和表情等。
            文明的举止是一种修养，是一种财富。
            细化目标：
            1、热爱祖国，升国旗奏国歌时自觉肃立。
            2、见到老师、客人主动问好。
            3、自觉使用“请”、“您好”“谢谢”、“对不起”、“再见”等礼貌用语。
            4、在接受别人的帮助时，要微笑着向别人致谢。
            5、向别人请教，态度要诚恳。
            6、不打架，不骂人，公共场所不喧哗。
            7、不给同学起绰号，不歧视身体有残疾的同学。
            8、当同学答错问题时，不起哄、不嘲笑。
            9、集合做到“快、静、齐”，观看比赛文明喝采。
          </p>
